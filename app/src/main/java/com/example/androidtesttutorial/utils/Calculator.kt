class Calculator {
    fun sum(
        num1: Double,
        num2: Double,
    ): Double {
        return num1 + num2
    }

    fun sub(
        num1: Double,
        num2: Double,
    ): Double {
        return num1 - num2
    }

    fun mul(
        num1: Double,
        num2: Double,
    ): Double {
        return num1 * num2
    }

    fun div(
        num1: Double,
        num2: Double,
    ): Double {
        return num1 / num2
    }
}
